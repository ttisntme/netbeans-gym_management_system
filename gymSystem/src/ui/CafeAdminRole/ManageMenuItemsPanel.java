    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
       int row = tblMenuItems.getSelectedRow();
       if(row < 0)
        {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
       MenuItem menuItem = (MenuItem) tblMenuItems.getValueAt(row, 0);
       for (MenuItem item : enterprise.getMenuItems()){
           if(item.equals(menuItem)){
               enterprise.getMenuItems().remove(item);
               break;
           }
       }
       JOptionPane.showMessageDialog(this, "Selected menu item has been deleted.", "Information", JOptionPane.INFORMATION_MESSAGE);
       populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed
    private boolean isValidDouble(String s){
            boolean isValidDouble = false;
          try
          {
             Double.parseDouble(s);
             isValidDouble = true;
          }
          catch (NumberFormatException ex)
          {
             // s is not an integer
          }
          return isValidDouble;
        }
    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
       String itemName = txtItemName.getText(); 
       String price = txtItemPrice.getText(); 
       ArrayList<MenuItem> menuItems = enterprise.getMenuItems(); 
        if (itemName.isEmpty() || price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please provide the missing menu Item information.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!isValidDouble(price)){
             JOptionPane.showMessageDialog(this, "Please provide a valid number for the Item price.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        MenuItem menuItem = new MenuItem(enterprise , itemName); 
        menuItem.setMenuItemPrice(Double.valueOf(price));
        menuItems.add(menuItem); 
        populateTable();
    }//GEN-LAST:event_btnAddItemActionPerformed

