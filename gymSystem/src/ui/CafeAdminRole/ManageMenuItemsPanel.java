      private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblMenuItems.getModel();
        model.setRowCount(0);    
          for(MenuItem menuItem : enterprise.getMenuItems()){
               Object[] row = new Object[2]; 
            row[0] = menuItem; 
            row[1] = menuItem.getPrice(); 
            model.addRow(row);
          }
      
    }
